-- Criar tabela
CREATE TABLE TBCONTATO (
    ID SERIAL PRIMARY KEY,
    NOME VARCHAR(100) NOT NULL,
    EMAIL VARCHAR(100),
    TELEFONE VARCHAR(25),
    DATACRIACAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    DATAATUALIZACAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

